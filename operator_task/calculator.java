package com.qa.operator_task;

public class calculator {

	public static int add(int a, int b) {
		int sum = a + b;
		return sum;
	}

	public static int multiply(int c, int d) {
		int multiply = c * d;
		return multiply;
	}

	public static int divide(int e, int f) {
		int divide = e / f;
		return divide;
	}

	public static int subtract(int g, int h) {
		int subtract = g - h;
		return subtract;

	}

}
